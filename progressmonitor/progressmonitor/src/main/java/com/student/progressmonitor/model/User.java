package com.student.progressmonitor.model;

import jakarta.persistence.*;
import lombok.Data;


@Table(name = "users")
@Data
public class User {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    private String name;

    @Column(unique = true)
    private String email;

    private String password;

    private String role; // ADMIN, TEACHER, PARENT
}
